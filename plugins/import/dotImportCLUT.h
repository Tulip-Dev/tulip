void HSBtoRGB(float inH, float inS, float inB, float &outR, float &outG, float &outB) {
  if (inS == 0.0f) {
    outR = outG = outB = inB * 255.0f;
  } else {
    float _h = inH * 6.0f, _i = int(_h), _1 = inB * (1.0f - inS),
          _2 = inB * (1.0f - inS * (_h - _i)), _3 = inB * (1.0f - inS * (1.0f - (_h - _i)));

    switch (int(_i)) {
    case 0:
      outR = inB * 255.0f;
      outG = _3 * 255.0f;
      outB = _1 * 255.0f;
      break;

    case 1:
      outR = _2 * 255.0f;
      outG = inB * 255.0f;
      outB = _1 * 255.0f;
      break;

    case 2:
      outR = _1 * 255.0f;
      outG = inB * 255.0f;
      outB = _3 * 255.0f;
      break;

    case 3:
      outR = _1 * 255.0f;
      outG = _2 * 255.0f;
      outB = inB * 255.0f;
      break;

    case 4:
      outR = _3 * 255.0f;
      outG = _1 * 255.0f;
      outB = inB * 255.0f;
      break;

    default:
      outR = inB * 255.0f;
      outG = _1 * 255.0f;
      outB = _2 * 255.0f;
      break;
    }
  }
}

bool DecodeColor(tlp::Color &outColor, const std::string &inValue) {
  // #RRGGBB ?
  {
    if (inValue.size() >= 7 && inValue[0] == '#') {
      unsigned int r, g, b;
      int n = sscanf(inValue.c_str(), "#%02x%02x%02x", &r, &g, &b);

      if (n == 3) {
        outColor = tlp::Color(r, g, b);
        return true;
      } else
        return false;
    }
  }

  // %f,%f,%f ?
  {
    float r, g, b;
    int n = sscanf(inValue.c_str(), "%f,%f,%f", &r, &g, &b);

    if (n == 3) {
      outColor = tlp::Color(uchar(r * 255.0f), uchar(g * 255.0f), uchar(b * 255.0f));
      return true;
    }
  }

  // %f %f %f ?
  {
    float r, g, b;
    int n = sscanf(inValue.c_str(), "%f %f %f", &r, &g, &b);

    if (n == 3) {
      outColor = tlp::Color(uchar(r * 255.0f), uchar(g * 255.0f), uchar(b * 255.0f));
      return true;
    }
  }

  // x11 name ?
  {
    unsigned int N = sizeof(X11Clut) / sizeof(X11Color);

    for (unsigned int i = 0; i < N; i++) {
      if (strcasecmp(inValue.c_str(), X11Clut[i].name) == 0) {
        float r, g, b;
        HSBtoRGB(float(X11Clut[i].h) / 255.0f, float(X11Clut[i].s) / 255.0f,
                 float(X11Clut[i].b) / 255.0f, r, g, b);
        outColor = tlp::Color(uchar(r), uchar(g), uchar(b));
        return true;
      }
    }
  }

  return false;
}
